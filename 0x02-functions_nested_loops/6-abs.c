/**
 * _abs - get the aboslute number of an integer
 * @x: the value that function return the absolute number
 *
 * Description: return the required results
 *
 * Return: return integer value 0
 *
 */
int _abs(int x)
{
	if (x >= 0)
		return (x);
	else
		return (x * -1);
	return (0);
}
